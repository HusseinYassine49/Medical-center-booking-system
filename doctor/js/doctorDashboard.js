document.getElementById('navbarToggle').addEventListener('click', function() {
    var sidebar = document.getElementById('navbar');
    if (sidebar.style.display === 'block') {
        sidebar.style.display = 'none';
    } else {
        sidebar.style.display = 'block';
    }
});
